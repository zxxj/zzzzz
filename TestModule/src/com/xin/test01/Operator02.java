package com.xin.test01;

// 赋值运算符
public class Operator02 {
    public static void main(String[] args) {

        // =
        int num1 = 10;
        int num2 = 20;
        int num3 = 10 + 30;

        // 求和:  +=
        int sum = 0;
        sum = num1 + num2;
        sum = sum + num3;
        System.out.println(sum);
    }
}
